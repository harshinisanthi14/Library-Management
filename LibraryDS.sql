    FirstName VARCHAR(50) NOT NULL,
    LastName VARCHAR(50) NOT NULL,
    Position VARCHAR(50),
    HireDate DATE,
    BranchID INT,
    PhoneNumber VARCHAR(15),
    Email VARCHAR(50),
    FOREIGN KEY (BranchID) REFERENCES LibraryBranches(BranchID)
);

